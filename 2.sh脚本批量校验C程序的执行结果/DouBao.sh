# 以下是一个使用 bash 编写的脚本，它可以完成你所要求的任务：

#!/bin/bash

# 遍历当前文件夹下的所有 c 语言源代码文件
for file in *.c; do
    # 编译源代码文件
    gcc $file -o ${file%.c}

    # 执行编译后的程序
    output="$(./${file%.c})"

    # 如果输出结尾没有换行，则添加换行
    if [[! $output =~ \n$ ]]; then
        output="$output\n"
    fi

    # 检查程序的输出是否为 "Hello, World!"（忽略末尾是否有多余的换行）
    if [[ $output == *"Hello, World!"* ]]; then
        echo "成功：$file 输出了 'Hello, World!'"
    else
        echo "失败：$file 没有输出 'Hello, World!'"
    fi

    # 删除编译的文件
    rm ${file%.c}
done

# 这个脚本会遍历当前文件夹下的所有 .c 文件，并使用 gcc 进行编译。然后，它会执行编译后的程序，并检查输出是否为 "Hello, World!"。如果输出结尾没有换行，则添加一个换行。最后，它会删除编译的文件。
# 请注意，这个脚本假设你的源代码文件都是有效的 C 语言代码，并且可以成功编译和执行。如果源代码文件有错误或者需要其他依赖项，可能会导致编译或执行失败。
# 另外，这个脚本会直接删除编译的文件，所以在使用之前，请确保你已经备份了重要的文件，或者你确定可以安全地删除这些文件。
