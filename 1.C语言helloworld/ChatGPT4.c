// 以下是一个简单的C语言程序，用于输出字符串“Hello, World!”：
#include <stdio.h> // 引入标准输入输出头文件

int main() {
    // 使用printf函数输出字符串
    printf("Hello, World!\n");
    return 0; // 程序正常结束返回0
}

// 这个程序首先包含了stdio.h头文件，这是因为它需要使用printf函数来进行输出。在main函数中，调用printf函数打印出了Hello, World!字符串，后面跟着一个换行符\n来确保输出后光标移动到下一行。最后，程序返回0，表示正常结束。
