// 以下是一个简单的 C 语言程序，用于输出字符串“Hello, World!”：

#include <stdio.h>

int main() {
    printf("Hello, World!");
    return 0;
}

// 在这个程序中，我们使用了printf函数来输出字符串“Hello, World!”。printf函数是 C 语言标准库中的一个输出函数，它可以将指定的文本输出到标准输出设备（通常是屏幕）上。
// main函数是 C 语言程序的入口点，程序的执行从这里开始。在main函数中，我们使用return 0;语句结束程序的执行，并返回 0 表示程序正常结束。
// 你可以将上述代码保存为一个.c文件（例如hello_world.c），然后使用 C 语言编译器编译并运行该程序，就可以在屏幕上看到输出的“Hello, World!”字符串了。希望这个示例对你有帮助！如果你还有其他问题，请随时提问。😄
